    Homo sapiens (literalmente en latín ‘el hombre sabio’),1​ comúnmente llamado ser humano, persona2​ u hombre3​ —este último en el sentido de ser racional, que no distingue entre ambos sexos—,4​ es una especie del orden de los primates perteneciente a la familia de los homínidos. El conjunto de personas o el género humano5​ también se conoce con la denominación genérica de humanos6​ y humanidad.7​ Los seres humanos poseen capacidades mentales que les permiten inventar, aprender y utilizar estructuras lingüísticas complejas, lógicas, matemáticas, escritura, música, ciencia y tecnología. Los seres humanos son animales sociales, capaces de concebir, transmitir y aprender conceptos totalmente abstractos.

